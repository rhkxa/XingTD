package com.gps808.app.models;

public class XbCar {
	private String vid;

	private String plateNo;

	private String tmnlName;

	private String simNo;

	private String tmnlNo;

	private String tmnlType;

	private String start;

	private String end;

	private String addr;

	public String getVid() {
		return vid;
	}

	public void setVid(String vid) {
		this.vid = vid;
	}

	public String getPlateNo() {
		return plateNo;
	}

	public void setPlateNo(String plateNo) {
		this.plateNo = plateNo;
	}

	public String getTmnlName() {
		return tmnlName;
	}

	public void setTmnlName(String tmnlName) {
		this.tmnlName = tmnlName;
	}

	public String getSimNo() {
		return simNo;
	}

	public void setSimNo(String simNo) {
		this.simNo = simNo;
	}

	public String getTmnlNo() {
		return tmnlNo;
	}

	public void setTmnlNo(String tmnlNo) {
		this.tmnlNo = tmnlNo;
	}

	public String getTmnlType() {
		return tmnlType;
	}

	public void setTmnlType(String tmnlType) {
		this.tmnlType = tmnlType;
	}

	public String getStart() {
		return start;
	}

	public void setStart(String start) {
		this.start = start;
	}

	public String getEnd() {
		return end;
	}

	public void setEnd(String end) {
		this.end = end;
	}

	public String getAddr() {
		return addr;
	}

	public void setAddr(String addr) {
		this.addr = addr;
	}

}
