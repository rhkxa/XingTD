package com.gps808.app.bean;

public class BnSpecs {
	private String spec_id;
	private String spec_name;
	private String spec_value_id;
	private String spec_image;
	private String spec_value;
	
	public String getSpec_id() {
		return spec_id;
	}

	public void setSpec_id(String spec_id) {
		this.spec_id = spec_id;
	}

	public String getSpec_name() {
		return spec_name;
	}

	public void setSpec_name(String spec_name) {
		this.spec_name = spec_name;
	}

	public String getSpec_value_id() {
		return spec_value_id;
	}

	public void setSpec_value_id(String spec_value_id) {
		this.spec_value_id = spec_value_id;
	}

	public String getSpec_image() {
		return spec_image;
	}

	public void setSpec_image(String spec_image) {
		this.spec_image = spec_image;
	}

	public String getSpec_value() {
		return spec_value;
	}

	public void setSpec_value(String spec_value) {
		this.spec_value = spec_value;
	}

}
