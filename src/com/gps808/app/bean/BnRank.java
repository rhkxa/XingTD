package com.gps808.app.bean;

public class BnRank {

	private String nickname;

	private String face;

	private int rank;

	private double monthly_icome;

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getFace() {
		return face;
	}

	public void setFace(String face) {
		this.face = face;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public double getMonthly_icome() {
		return monthly_icome;
	}

	public void setMonthly_icome(double monthly_icome) {
		this.monthly_icome = monthly_icome;
	}
	
}
